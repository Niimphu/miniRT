#include "matrix.h"

t_matrix	m_matrix_mul(t_matrix t, t_matrix r)
{
	t_matrix	m;
	int			i;
	int			j;
	int			k;

	i = 0;
	while (i < 4)
	{
		j = 0;
		while (j < 4)
		{
			m.m[i][j] = 0.0;
			k = 0;
			while (k < 4)
			{
				m.m[i][j] += t.m[i][k] * r.m[k][j];
				k++;
			}
			j++;
		}
		i++;
	}
	return (m);
}

t_matrix	identity_matrix(void)
{
	t_matrix	m;

	m.m[0][0] = 1.0;
	m.m[0][1] = 0.0;
	m.m[0][2] = 0.0;
	m.m[0][3] = 0.0;
	m.m[1][0] = 0.0;
	m.m[1][1] = 1.0;
	m.m[1][2] = 0.0;
	m.m[1][3] = 0.0;
	m.m[2][0] = 0.0;
	m.m[2][1] = 0.0;
	m.m[2][2] = 1.0;
	m.m[2][3] = 0.0;
	m.m[3][0] = 0.0;
	m.m[3][1] = 0.0;
	m.m[3][2] = 0.0;
	m.m[3][3] = 1.0;
	return (m);
}
